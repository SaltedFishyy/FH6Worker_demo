exports.main = async (event = {}) => {
  try {
    const payload = normalizeEventPayload(event);
    const data = generateRoadStaticTuneBaseline(payload);
    return {
      success: true,
      data,
    };
  } catch (err) {
    return {
      success: false,
      errorCode: "VALIDATION_ERROR",
      message: err && err.message ? err.message : "Failed to calculate tune.",
    };
  }
};

exports._internal = {
  generateRoadStaticTuneBaseline,
};

function normalizeEventPayload(event) {
  if (event && event.payload && typeof event.payload === "object") {
    return event.payload;
  }
  if (event && event.data && typeof event.data === "object") {
    return event.data;
  }
  const payload = { ...event };
  delete payload.type;
  return payload;
}

function generateRoadStaticTuneBaseline(rawInput) {
  const input = normalizeRoadStaticBaselineInput(rawInput || {});
  const carClass = classFromPI(input.pi);
  const useCase = input.useCase;
  const versionName = input.versionName || baselineDefaultVersionName(useCase);

  const draft = normalizeTuneProfilePower({
    carName: input.carName,
    carOrdinal: input.carOrdinal,
    carCategory: input.carCategory,
    carClass,
    pi: input.pi,
    drivetrain: input.drivetrain,
    useCase,
    versionName,
    weightKG: input.weightKG,
    frontWeightPct: input.frontWeightPct,
    powerKW: input.powerKW,
    torqueNM: input.torqueNM,
    redlineRPM: input.redlineRPM,
    notes: `Generated by ${useCase} Static Baseline.`,
  });

  const result = {
    profileDraft: draft,
    confidence: "high",
    generatedFields: [],
    tierRecommendations: [],
    skippedFields: [],
    warnings: [
      "当前结果是快速调校基线，不代表一次即可达到完美调校。请结合具体赛道表现、车辆反馈和个人驾驶习惯，在实测后继续逐项微调。",
    ],
    nextTestPlan: [
      baselineNextTestPlanIntro(useCase),
      "首次测试优先观察胎温、轮胎滑移平衡、刹车稳定性和车身是否触底。",
      "齿轮调校建议在后续结合动力输出和实测速度表现继续修正，不要只依赖AI估算。",
    ],
  };

  const add = (fieldKey, group, value, reason) => {
    setProfileField(result.profileDraft, fieldKey, value);
    result.generatedFields.push({
      fieldKey,
      group,
      value: result.profileDraft[fieldKey],
      unit: tunePlanFieldUnit(fieldKey),
      reason,
      defaultSelected: true,
    });
  };

  const addMeta = (fieldKey, group, value, unit, reason) => {
    if (value === null || value === undefined) return;
    result.generatedFields.push({
      fieldKey,
      group,
      value,
      unit,
      reason,
      defaultSelected: true,
    });
  };

  const skip = (fieldKey, group, reason, message) => {
    result.skippedFields.push({
      fieldKey,
      group,
      reason,
      message,
    });
  };

  const addTier = (fieldKey, group, tier, applicable, reason) => {
    result.tierRecommendations.push({
      fieldKey,
      group,
      tier,
      reason,
      applicable: Boolean(applicable),
    });
    if (!applicable) {
      skip(
        fieldKey,
        group,
        "not_confirmed_adjustable",
        "Confirm this tuning item is unlocked before using the tier recommendation."
      );
    }
  };

  addMeta("weightKG", "power", result.profileDraft.weightKG, "kg", "Required baseline input.");
  addMeta("frontWeightPct", "power", result.profileDraft.frontWeightPct, "%", "Required baseline input.");
  addMeta("powerKW", "power", result.profileDraft.powerKW, "kW", "Optional profile context.");
  addMeta("torqueNM", "power", result.profileDraft.torqueNM, "Nm", "Optional profile context.");
  addMeta("redlineRPM", "power", result.profileDraft.redlineRPM, "rpm", "Static gearing input and power-band context.");

  const tire = baselineTirePressures(useCase, input.drivetrain, input.tireCompound, input.weightKG);
  add("frontTirePressure", "tire", tire.front, baselineReason(useCase, "cold-pressure baseline in BAR from tire compound, vehicle weight, and drivetrain split."));
  add("rearTirePressure", "tire", tire.rear, baselineReason(useCase, "cold-pressure baseline in BAR from tire compound, vehicle weight, and drivetrain split."));

  const alignment = baselineAlignment(useCase, carClass, input.drivetrain, input.frontWeightPct, input.balanceBias);
  add("frontCamber", "alignment", alignment.frontCamber, baselineReason(useCase, "alignment baseline from PI class, front weight distribution, drivetrain, and balance bias."));
  add("rearCamber", "alignment", alignment.rearCamber, baselineReason(useCase, "alignment baseline from PI class, front weight distribution, drivetrain, and balance bias."));
  add("frontToe", "alignment", alignment.frontToe, baselineReason(useCase, "toe baseline; only strong balance bias moves Road away from neutral, while Drift uses fixed steering response."));
  add("rearToe", "alignment", alignment.rearToe, baselineReason(useCase, "toe baseline; only strong balance bias moves Road away from neutral, while Drift uses fixed steering response."));
  add("caster", "alignment", alignment.caster, baselineReason(useCase, "caster baseline from PI class, front weight distribution, and drivetrain."));

  const springFactor = springFactorForBaseline(useCase, carClass);
  const springs = baselineSprings(useCase, input.weightKG, input.frontWeightPct, springFactor, input.drivetrain);
  add("frontSpring", "springs", springs.front, "FH6 kgf/mm menu value calculated from vehicle weight, axle weight distribution, and PI spring factor.");
  add("rearSpring", "springs", springs.rear, "FH6 kgf/mm menu value calculated from vehicle weight, axle weight distribution, and PI spring factor.");

  const arb = baselineARB(useCase, input.frontWeightPct, carClass, input.drivetrain);
  add("frontArb", "antiroll", arb.front, baselineReason(useCase, "lateral load-transfer baseline."));
  add("rearArb", "antiroll", arb.rear, baselineReason(useCase, "lateral load-transfer baseline."));

  const damping = baselineDamping(useCase, input.weightKG, input.frontWeightPct, carClass, springFactor, springs.front, springs.rear, input.drivetrain);
  add("frontRebound", "damping", damping.frontRebound, baselineReason(useCase, "damping slider baseline from front axle weight, PI/use-case minimum bump, spring-factor multiplier, and bump/rebound ratio."));
  add("rearRebound", "damping", damping.rearRebound, "Road damping slider baseline offset from front rebound by front/rear spring display difference.");
  add("frontBump", "damping", damping.frontBump, baselineReason(useCase, "bump slider baseline from front axle weight, PI/use-case minimum bump, and spring-factor multiplier."));
  add("rearBump", "damping", damping.rearBump, "Road bump slider baseline offset from front bump by front/rear spring display difference.");

  const rideTier = baselineRideHeightTier(useCase);
  const aeroTier = baselineAeroTier(useCase);
  addTier("frontRideHeight", "springs", rideTier.tier, input.frontRideHeightAdjustable, rideTier.reason);
  addTier("rearRideHeight", "springs", rideTier.tier, input.rearRideHeightAdjustable, rideTier.reason);
  addTier("frontAero", "aero", aeroTier.tier, input.frontAeroAdjustable, aeroTier.reason);
  addTier("rearAero", "aero", aeroTier.tier, input.rearAeroAdjustable, aeroTier.reason);

  add("brakeBalance", "brake", baselineBrakeBalance(useCase, input.drivetrain, input.frontWeightPct, input.frontTireWidthMm, input.rearTireWidthMm), baselineReason(useCase, "brake-balance baseline by use case, drivetrain, front weight, and optional tire width split."));
  add("brakePressure", "brake", 100, "Neutral brake-pressure starting point.");
  appendDifferentialBaseline(
    result,
    useCase,
    carClass,
    input.drivetrain,
    input.weightKG,
    input.frontWeightPct,
    input.balanceBias,
    input.powerKW,
    input.torqueNM,
    add,
    skip
  );

  const gearing = baselineGearing(input);
  if (gearing.fields.length > 0) {
    gearing.fields.forEach((field) => add(field.key, "gearing", field.value, field.reason));
    for (let gear = input.gearCount + 1; gear <= 10; gear += 1) {
      skip(`gear${gear}`, "gearing", "locked_or_unavailable", "Gear is above the selected gear count and is treated as not unlocked.");
    }
  } else {
    skip("finalDrive", "gearing", gearing.reason, gearing.message);
    for (let gear = 1; gear <= 10; gear += 1) {
      skip(`gear${gear}`, "gearing", gearing.reason, gearing.message);
    }
  }

  appendDriftTargetSpeedWarning(result, input);
  applyRoadBaselineBiases(result, input);
  result.generatedFields.sort((a, b) => {
    if (a.group === b.group) return a.fieldKey.localeCompare(b.fieldKey);
    return a.group.localeCompare(b.group);
  });
  result.profileDraft = normalizeTuneProfilePower(result.profileDraft);
  return result;
}

function normalizeRoadStaticBaselineInput(raw) {
  const useCase = normalizeStaticBaselineUseCase(raw.useCase);
  const input = {
    carName: stringValue(raw.carName),
    versionName: stringValue(raw.versionName),
    useCase,
    carOrdinal: optionalInteger(raw.carOrdinal),
    carCategory: optionalInteger(raw.carCategory),
    pi: requiredInteger(raw.pi, "PI"),
    drivetrain: stringValue(raw.drivetrain).toUpperCase(),
    tireCompound: normalizeTireCompoundForUseCase(raw.tireCompound, useCase),
    weightKG: requiredInteger(raw.weightKG, "weight kg"),
    frontWeightPct: requiredInteger(raw.frontWeightPct, "front weight percentage"),
    powerKW: optionalNumber(raw.powerKW),
    torqueNM: optionalNumber(raw.torqueNM),
    redlineRPM: optionalInteger(raw.redlineRPM),
    gearCount: optionalInteger(raw.gearCount),
    tireDiameterCm: optionalNumber(raw.tireDiameterCm),
    frontTireDiameterCm: optionalNumber(raw.frontTireDiameterCm),
    rearTireDiameterCm: optionalNumber(raw.rearTireDiameterCm),
    frontTireWidthMm: optionalInteger(raw.frontTireWidthMm),
    rearTireWidthMm: optionalInteger(raw.rearTireWidthMm),
    targetTopSpeedKmh: optionalInteger(raw.targetTopSpeedKmh),
    frontRideHeightMinCm: optionalNumber(raw.frontRideHeightMinCm),
    frontRideHeightMaxCm: optionalNumber(raw.frontRideHeightMaxCm),
    rearRideHeightMinCm: optionalNumber(raw.rearRideHeightMinCm),
    rearRideHeightMaxCm: optionalNumber(raw.rearRideHeightMaxCm),
    frontAeroMinKgf: optionalNumber(raw.frontAeroMinKgf),
    frontAeroMaxKgf: optionalNumber(raw.frontAeroMaxKgf),
    rearAeroMinKgf: optionalNumber(raw.rearAeroMinKgf),
    rearAeroMaxKgf: optionalNumber(raw.rearAeroMaxKgf),
    frontRideHeightAdjustable: Boolean(raw.frontRideHeightAdjustable),
    rearRideHeightAdjustable: Boolean(raw.rearRideHeightAdjustable),
    frontAeroAdjustable: Boolean(raw.frontAeroAdjustable),
    rearAeroAdjustable: Boolean(raw.rearAeroAdjustable),
    balanceBias: normalizeBaselineBias(optionalNumber(raw.balanceBias)),
    stiffnessBias: normalizeBaselineBias(optionalNumber(raw.stiffnessBias)),
    speedBias: normalizeBaselineBias(optionalNumber(raw.speedBias)),
    tractionBias: normalizeBaselineBias(optionalNumber(raw.tractionBias)),
    shiftBias: normalizeBaselineBias(optionalNumber(raw.shiftBias)),
    trackFeedbackScene: normalizeTrackFeedbackScene(raw.trackFeedbackScene),
    trackFeedbackSymptom: normalizeTrackFeedbackSymptom(raw.trackFeedbackScene, raw.trackFeedbackSymptom),
    correctionStrengthBias: normalizeBaselineBias(optionalNumber(raw.correctionStrengthBias)),
    driftFeedbackSymptom: normalizeDriftFeedbackSymptom(raw.driftFeedbackSymptom),
    driftCorrectionStrengthBias: normalizeBaselineBias(optionalNumber(raw.driftCorrectionStrengthBias)),
    offroadFeedbackSymptom: normalizeOffroadFeedbackSymptom(raw.offroadFeedbackSymptom),
    offroadCorrectionStrengthBias: normalizeBaselineBias(optionalNumber(raw.offroadCorrectionStrengthBias)),
  };

  if (!supportedUseCases().includes(input.useCase)) {
    throw new Error("use case must be Road, Rally, Offroad, Drag, or Drift");
  }
  if (!supportedTireCompounds().includes(input.tireCompound)) {
    throw new Error(`tire compound must be one of: ${supportedTireCompounds().join(", ")}`);
  }
  if (input.pi < 100 || input.pi > 999) {
    throw new Error("PI must be between 100 and 999");
  }
  if (!isWholeNumber(input.weightKG) || input.weightKG < 300 || input.weightKG > 3000) {
    throw new Error("weight kg must be an integer between 300 and 3000");
  }
  if (!isWholeNumber(input.frontWeightPct) || input.frontWeightPct < 1 || input.frontWeightPct > 99) {
    throw new Error("front weight percentage must be an integer between 1 and 99");
  }
  if (input.redlineRPM !== null && (!isWholeNumber(input.redlineRPM) || input.redlineRPM < 1000 || input.redlineRPM > 20000)) {
    throw new Error("redline RPM must be an integer between 1000 and 20000");
  }
  if (input.gearCount !== null && (input.gearCount < 2 || input.gearCount > 10)) {
    throw new Error("gear count must be between 2 and 10");
  }
  if (input.tireDiameterCm !== null && (input.tireDiameterCm < 40 || input.tireDiameterCm > 120)) {
    throw new Error("tire diameter must be between 40 cm and 120 cm");
  }
  if (input.frontTireDiameterCm !== null && (input.frontTireDiameterCm < 40 || input.frontTireDiameterCm > 120)) {
    throw new Error("front tire diameter must be between 40 cm and 120 cm");
  }
  if (input.rearTireDiameterCm !== null && (input.rearTireDiameterCm < 40 || input.rearTireDiameterCm > 120)) {
    throw new Error("rear tire diameter must be between 40 cm and 120 cm");
  }
  validateOptionalTireWidth(input.frontTireWidthMm, "front tire width");
  validateOptionalTireWidth(input.rearTireWidthMm, "rear tire width");
  if (input.targetTopSpeedKmh !== null) {
    if (input.useCase === "Drift") {
      if (!isWholeNumber(input.targetTopSpeedKmh) || input.targetTopSpeedKmh < 20 || input.targetTopSpeedKmh > 260) {
        throw new Error("target drift speed must be an integer between 20 and 260 km/h");
      }
    } else if (!isWholeNumber(input.targetTopSpeedKmh) || input.targetTopSpeedKmh <= 0 || input.targetTopSpeedKmh > 600) {
      throw new Error("target top speed must be an integer between 1 and 600");
    }
  }
  validateBaselineBias(input.balanceBias, "balance bias");
  validateBaselineBias(input.stiffnessBias, "stiffness bias");
  validateBaselineBias(input.speedBias, "speed bias");
  validateBaselineBias(input.tractionBias, "traction bias");
  validateBaselineBias(input.shiftBias, "shift bias");
  validateBaselineBias(input.correctionStrengthBias, "correction strength bias");
  validateBaselineBias(input.driftCorrectionStrengthBias, "drift correction strength bias");
  validateBaselineBias(input.offroadCorrectionStrengthBias, "offroad correction strength bias");
  if (!["FWD", "RWD", "AWD"].includes(input.drivetrain)) {
    throw new Error("drivetrain must be FWD, RWD, or AWD");
  }
  if (!supportsTrackFeedbackUseCase(input.useCase)) {
    input.trackFeedbackScene = "";
    input.trackFeedbackSymptom = "";
  }
  if (!equalsIgnoreCase(input.useCase, "Drift")) {
    input.driftFeedbackSymptom = "";
  }
  if (!equalsIgnoreCase(input.useCase, "Offroad")) {
    input.offroadFeedbackSymptom = "";
  }
  return input;
}

function normalizeStaticBaselineUseCase(value) {
  const normalized = stringValue(value).toLowerCase();
  if (normalized === "" || normalized === "road" || normalized === "公路") return "Road";
  if (normalized === "drift" || normalized === "漂移") return "Drift";
  if (normalized === "rally" || normalized === "拉力") return "Rally";
  if (normalized === "offroad" || normalized === "off-road" || normalized === "越野") return "Offroad";
  if (normalized === "drag" || normalized === "直线" || normalized === "直线加速") return "Drag";
  return stringValue(value);
}

function supportedUseCases() {
  return ["Road", "Rally", "Offroad", "Drag", "Drift"];
}

function baselineDefaultVersionName(useCase) {
  switch (stringValue(useCase)) {
    case "Drift":
      return "Drift Baseline";
    case "Rally":
      return "Rally Baseline";
    case "Offroad":
      return "Offroad Baseline";
    case "Drag":
      return "Drag Baseline";
    default:
      return "Road Baseline";
  }
}

function baselineNextTestPlanIntro(useCase) {
  if (equalsIgnoreCase(useCase, "Drift")) {
    return "先用当前基线完成一次可控漂移测试，再调整多个系统。";
  }
  if (equalsIgnoreCase(useCase, "Rally")) {
    return "先用当前基线完成一次混合路面拉力测试，重点观察落地稳定性和连续弯响应。";
  }
  if (equalsIgnoreCase(useCase, "Offroad")) {
    return "先用当前基线完成一次越野颠簸路段测试，重点观察悬挂行程和抓地连续性。";
  }
  if (equalsIgnoreCase(useCase, "Drag")) {
    return "先用当前基线完成一次直线起步和全油门测试，重点观察起步打滑和换挡后转速。";
  }
  return "先用当前基线完成一次干净的公路测试，再调整多个系统。";
}

function baselineReason(useCase, text) {
  return `${stringValue(useCase) || "Road"} ${text}`.trim();
}

function normalizeBaselineBias(value) {
  return value === null || value === undefined || value === 0 ? 100 : value;
}

function validateBaselineBias(value, name) {
  if (!isWholeNumber(value) || value < 50 || value > 150) {
    throw new Error(`${name} must be an integer between 50 and 150`);
  }
}

function validateOptionalTireWidth(value, name) {
  if (value === null || value === undefined) return;
  if (!isWholeNumber(value) || value < 100 || value > 455) {
    throw new Error(`${name} must be an integer between 100 and 455 mm`);
  }
}

function normalizeTrackFeedbackScene(value) {
  const normalized = stringValue(value);
  if (["lowSpeedCorner", "highSpeedCorner"].includes(normalized)) return normalized;
  return "";
}

function normalizeTrackFeedbackSymptom(scene, value) {
  const normalizedScene = normalizeTrackFeedbackScene(scene);
  if (!normalizedScene) return "";
  const normalizedSymptom = stringValue(value);
  return supportedTrackFeedbackSymptoms(normalizedScene).includes(normalizedSymptom) ? normalizedSymptom : "";
}

function supportedTrackFeedbackSymptoms(scene) {
  switch (normalizeTrackFeedbackScene(scene)) {
    case "lowSpeedCorner":
      return ["entryUndersteer", "midUndersteer", "exitWheelspin", "brakingInstability"];
    case "highSpeedCorner":
      return ["highSpeedEntryInstability", "midUndersteer", "highSpeedOversteer", "bodyBounce"];
    default:
      return [];
  }
}

function normalizeDriftFeedbackSymptom(value) {
  const normalized = stringValue(value);
  return supportedDriftFeedbackSymptoms().includes(normalized) ? normalized : "";
}

function supportedDriftFeedbackSymptoms() {
  return [
    "initiationDifficult",
    "initiationTooSensitive",
    "angleTooSmall",
    "angleTooLarge",
    "rearWheelspin",
    "rearGripTooStrong",
  ];
}

function normalizeOffroadFeedbackSymptom(value) {
  const normalized = stringValue(value);
  return supportedOffroadFeedbackSymptoms().includes(normalized) ? normalized : "";
}

function supportedOffroadFeedbackSymptoms() {
  return [
    "bumpKick",
    "bottomingOut",
    "landingUnstable",
    "lazyTurnIn",
    "exitWheelspin",
    "highSpeedFloat",
  ];
}

function supportsTrackFeedbackUseCase(useCase) {
  return equalsIgnoreCase(useCase, "Road") || equalsIgnoreCase(useCase, "Rally");
}

function classFromPI(pi) {
  if (pi <= 400) return "D";
  if (pi <= 500) return "C";
  if (pi <= 600) return "B";
  if (pi <= 700) return "A";
  if (pi <= 800) return "S1";
  if (pi <= 900) return "S2";
  if (pi <= 998) return "R";
  return "X";
}

function baselineTirePressures(useCase, drivetrain, tireCompound, weightKG) {
  if (equalsIgnoreCase(useCase, "Drift")) {
    return baselineDriftTirePressures(weightKG);
  }
  if (equalsIgnoreCase(useCase, "Drag")) {
    return baselineDragTirePressures(drivetrain, weightKG);
  }
  if (equalsIgnoreCase(useCase, "Rally") || equalsIgnoreCase(useCase, "Offroad")) {
    return baselineLooseSurfaceTirePressures(useCase, drivetrain, tireCompound, weightKG);
  }
  const base = tireCompoundBasePressureBar(tireCompound);
  const weightAdj = tirePressureWeightAddBar(weightKG);
  const split = tirePressureSplitBar(drivetrain);
  const pressureClamp = tirePressureClampBar(useCase);
  return {
    front: clamp(roundTunePlanStep(base + weightAdj + split.front, 0.01), pressureClamp.min, pressureClamp.max),
    rear: clamp(roundTunePlanStep(base + weightAdj + split.rear, 0.01), pressureClamp.min, pressureClamp.max),
  };
}

function baselineDriftTirePressures(weightKG) {
  const front = clamp(roundTunePlanStep(2.10 + tirePressureWeightAddBar(weightKG), 0.01), 2.10, 2.60);
  const rear = clamp(roundTunePlanStep(front + 0.40, 0.01), 2.50, 3.00);
  return { front, rear };
}

function baselineLooseSurfaceTirePressures(useCase, drivetrain, tireCompound, weightKG) {
  const base = tireCompoundBasePressureBar(tireCompound);
  const weightAdj = tirePressureWeightAddBar(weightKG) * 0.7;
  const split = tirePressureSplitBar(drivetrain);
  const pressureClamp = tirePressureClampBar(useCase);
  const looseDrop = equalsIgnoreCase(useCase, "Offroad") ? -0.06 : -0.02;
  return {
    front: clamp(roundTunePlanStep(base + weightAdj + split.front * 0.5 + looseDrop, 0.01), pressureClamp.min, pressureClamp.max),
    rear: clamp(roundTunePlanStep(base + weightAdj + split.rear * 0.5 + looseDrop, 0.01), pressureClamp.min, pressureClamp.max),
  };
}

function baselineDragTirePressures(drivetrain, weightKG) {
  const weightAdj = tirePressureWeightAddBar(weightKG) * 0.5;
  switch (stringValue(drivetrain).toUpperCase()) {
    case "FWD":
      return {
        front: clamp(roundTunePlanStep(1.78 + weightAdj, 0.01), 1.50, 2.35),
        rear: clamp(roundTunePlanStep(2.28 + weightAdj, 0.01), 1.80, 2.55),
      };
    case "AWD":
      return {
        front: clamp(roundTunePlanStep(1.88 + weightAdj, 0.01), 1.55, 2.45),
        rear: clamp(roundTunePlanStep(1.82 + weightAdj, 0.01), 1.50, 2.40),
      };
    default:
      return {
        front: clamp(roundTunePlanStep(2.30 + weightAdj, 0.01), 1.85, 2.55),
        rear: clamp(roundTunePlanStep(1.62 + weightAdj, 0.01), 1.45, 2.20),
      };
  }
}

function normalizeTireCompound(value) {
  const compound = stringValue(value).toLowerCase();
  return compound || "sport";
}

function normalizeTireCompoundForUseCase(value, useCase) {
  const compound = stringValue(value).toLowerCase();
  if (!compound) {
    if (equalsIgnoreCase(useCase, "Drift")) return "drift";
    if (equalsIgnoreCase(useCase, "Rally")) return "rally";
    if (equalsIgnoreCase(useCase, "Offroad")) return "offroad";
    if (equalsIgnoreCase(useCase, "Drag")) return "drag";
    return "sport";
  }
  return normalizeTireCompound(compound);
}

function supportedTireCompounds() {
  return ["stock", "street", "sport", "semi", "slick", "rally", "offroad", "drift", "drag", "snow"];
}

function tireCompoundBasePressureBar(tireCompound) {
  switch (normalizeTireCompound(tireCompound)) {
    case "stock":
    case "street":
      return 2.14;
    case "semi":
      return 2.21;
    case "slick":
      return 2.24;
    case "rally":
      return 2.03;
    case "offroad":
    case "snow":
      return 2.00;
    case "drift":
      return 1.52;
    case "drag":
      return 1.72;
    default:
      return 2.17;
  }
}

function tirePressureWeightAddBar(weightKG) {
  if (weightKG < 900) return -0.07;
  if (weightKG < 1200) return -0.03;
  if (weightKG < 1500) return 0;
  if (weightKG < 1700) return 0.03;
  if (weightKG < 1900) return 0.07;
  return 0.10;
}

