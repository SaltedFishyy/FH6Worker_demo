function applyRoadBaselineBiases(result, input) {
  const balanceAxis = baselineBiasAxis(input.balanceBias);
  const stiffnessAxis = baselineBiasAxis(input.stiffnessBias);
  const speedAxis = baselineBiasAxis(input.speedBias);
  const tractionAxis = baselineBiasAxis(input.tractionBias);
  const shiftAxis = baselineBiasAxis(input.shiftBias);
  const correctionMultiplier = normalizeBaselineBias(input.correctionStrengthBias) / 100;

  if (balanceAxis !== 0) {
    const note = `Balance bias ${input.balanceBias.toFixed(0)} adjusts front/rear ratio for stability versus rotation.`;
    const frontRatio = 1 - 0.08 * balanceAxis;
    const rearRatio = 1 + 0.08 * balanceAxis;
    const frontSupportRatio = 1 - 0.04 * balanceAxis;
    const rearSupportRatio = 1 + 0.04 * balanceAxis;
    scaleBaselineGeneratedField(result, "frontArb", frontRatio, note);
    scaleBaselineGeneratedField(result, "rearArb", rearRatio, note);
    scaleBaselineGeneratedField(result, "frontSpring", frontSupportRatio, note);
    scaleBaselineGeneratedField(result, "rearSpring", rearSupportRatio, note);
    scaleBaselineGeneratedField(result, "frontRebound", frontSupportRatio, note);
    scaleBaselineGeneratedField(result, "rearRebound", rearSupportRatio, note);
    scaleBaselineGeneratedField(result, "frontBump", frontSupportRatio, note);
    scaleBaselineGeneratedField(result, "rearBump", rearSupportRatio, note);
    adjustBaselineGeneratedField(result, "brakeBalance", (value) => clamp(value - 2 * balanceAxis, 45, 65), note);
  }

  if (stiffnessAxis !== 0) {
    const note = `Stiffness bias ${input.stiffnessBias.toFixed(0)} scales roll, spring, and damping support.`;
    const ratio = 1 + 0.12 * stiffnessAxis;
    ["frontArb", "rearArb", "frontSpring", "rearSpring", "frontRebound", "rearRebound", "frontBump", "rearBump"].forEach((key) => {
      scaleBaselineGeneratedField(result, key, ratio, note);
    });
  }

  if (speedAxis !== 0) {
    let note = `Speed bias ${input.speedBias.toFixed(0)} adjusts final drive toward top speed or acceleration while preserving gear spacing.`;
    if (equalsIgnoreCase(input.useCase, "Drift")) {
      note = `Drift speed bias ${input.speedBias.toFixed(0)} adjusts final drive toward higher-speed drift or lower-speed high-RPM drift while preserving gear spacing.`;
    }
    const ratio = 1 - 0.08 * speedAxis;
    scaleBaselineGeneratedField(result, "finalDrive", ratio, note);
  }

  applyRoadTireWidthBiases(result, input);
  applyGearFeedbackBiases(result, input, tractionAxis, shiftAxis);
  applyTrackFeedbackBiases(result, input, correctionMultiplier);
  applyDriftFeedbackBiases(result, input);
  applyOffroadFeedbackBiases(result, input);
}

function applyRoadTireWidthBiases(result, input) {
  if (!equalsIgnoreCase(input.useCase, "Road")) return;
  const axis = tireWidthBalanceAxis(input.frontTireWidthMm, input.rearTireWidthMm);
  if (axis === 0) return;
  const note = `Front/rear tire width split ${input.frontTireWidthMm}/${input.rearTireWidthMm} mm adjusts road brake and drivetrain load.`;
  const frontLimitDelta = clamp(axis, -1, 1.5);
  adjustBaselineGeneratedField(result, "frontDiffAccel", (value) => clamp(value - 1.5 * frontLimitDelta, 15, 35), note);
  adjustBaselineGeneratedField(result, "frontDiffDecel", (value) => clamp(value - 1.0 * frontLimitDelta, 0, 20), note);
  adjustBaselineGeneratedField(result, "centerDiffBalance", (value) => clamp(value + 2.5 * frontLimitDelta, 55, 82), note);
  adjustBaselineGeneratedField(result, "frontTirePressure", (value) => clamp(value - 0.02 * frontLimitDelta, 1.00, 4.00), note);
}

function applyGearFeedbackBiases(result, input, tractionAxis, shiftAxis) {
  if (!hasGeneratedGearing(result, input)) return;

  if (tractionAxis !== 0) {
    const note = `Traction bias ${input.tractionBias.toFixed(0)} adjusts launch and corner-exit gearing; differential compensation is intentionally small.`;
    scaleBaselineGeneratedField(result, "finalDrive", 1 + 0.035 * tractionAxis, note);
    scaleBaselineGeneratedField(result, "gear1", 1 + 0.060 * tractionAxis, note);
    scaleBaselineGeneratedField(result, "gear2", 1 + 0.045 * tractionAxis, note);
    scaleBaselineGeneratedField(result, "gear3", 1 + 0.025 * tractionAxis, note);
    const diffDelta = tractionAxis < 0 ? 2.0 * tractionAxis : 1.5 * tractionAxis;
    ["frontDiffAccel", "rearDiffAccel"].forEach((key) => {
      adjustBaselineGeneratedField(result, key, (value) => clamp(value + diffDelta, 0, 100), note);
    });
  }

  if (shiftAxis !== 0) {
    const note = `Shift bias ${input.shiftBias.toFixed(0)} adjusts intermediate gear spacing without changing final drive.`;
    const gearCount = input.gearCount || 0;
    const firstMiddle = 2;
    const lastMiddle = Math.max(firstMiddle, gearCount - 1);
    const midpoint = (firstMiddle + lastMiddle) / 2;
    const spread = Math.max(1, (lastMiddle - firstMiddle) / 2);
    for (let gear = firstMiddle; gear <= lastMiddle; gear += 1) {
      const centerWeight = clamp(1 - Math.abs(gear - midpoint) / spread * 0.35, 0.65, 1);
      scaleBaselineGeneratedField(result, `gear${gear}`, 1 + 0.050 * shiftAxis * centerWeight, note);
    }
  }
}

function hasGeneratedGearing(result, input) {
  if (!input || input.gearCount === null || input.gearCount === undefined) return false;
  if (typeof result.profileDraft.finalDrive !== "number") return false;
  return typeof result.profileDraft.gear1 === "number";
}

function applyTrackFeedbackBiases(result, input, correctionMultiplier) {
  if (!supportsTrackFeedbackUseCase(input.useCase)) return;
  const scene = normalizeTrackFeedbackScene(input.trackFeedbackScene);
  const symptom = normalizeTrackFeedbackSymptom(scene, input.trackFeedbackSymptom);
  if (!scene || !symptom) return;
  const strength = clamp(correctionMultiplier, 0.5, 1.5);
  const profile = trackFeedbackProfile(input.useCase);
  const note = `Track feedback ${scene}/${symptom} applies a ${input.correctionStrengthBias.toFixed(0)} strength secondary correction.${profile.note}`;
  const ratio = (value) => 1 + value * strength;
  const delta = (value) => value * strength;

  if (scene === "lowSpeedCorner") {
    switch (symptom) {
      case "entryUndersteer":
        scaleBaselineGeneratedField(result, "frontArb", ratio(-0.035 * profile.arb), note);
        scaleBaselineGeneratedField(result, "rearArb", ratio(0.025 * profile.arb), note);
        scaleBaselineGeneratedField(result, "frontSpring", ratio(-0.025 * profile.spring), note);
        scaleBaselineGeneratedField(result, "frontRebound", ratio(-0.045 * profile.damping), note);
        scaleBaselineGeneratedField(result, "frontBump", ratio(-0.035 * profile.damping), note);
        adjustBaselineGeneratedField(result, "frontCamber", (value) => value - delta(0.1 * profile.camber), note);
        adjustBaselineGeneratedField(result, "brakeBalance", (value) => clamp(value - delta(1.0 * profile.brake), 45, 65), note);
        reduceDecelLock(result, delta(1.0 * profile.diff), note);
        break;
      case "midUndersteer":
        scaleBaselineGeneratedField(result, "frontArb", ratio(-0.045 * profile.arb), note);
        scaleBaselineGeneratedField(result, "rearArb", ratio(0.035 * profile.arb), note);
        scaleBaselineGeneratedField(result, "frontSpring", ratio(-0.040 * profile.spring), note);
        scaleBaselineGeneratedField(result, "rearSpring", ratio(0.015 * profile.spring), note);
        scaleBaselineGeneratedField(result, "frontRebound", ratio(-0.030 * profile.damping), note);
        scaleBaselineGeneratedField(result, "frontBump", ratio(-0.035 * profile.damping), note);
        adjustBaselineGeneratedField(result, "frontCamber", (value) => value - delta(0.1 * profile.camber), note);
        reduceDecelLock(result, delta(1.0 * profile.diff), note);
        break;
      case "exitWheelspin":
        scaleBaselineGeneratedField(result, "finalDrive", ratio(-0.025 * profile.gear), note);
        scaleBaselineGeneratedField(result, "gear1", ratio(-0.030 * profile.gear), note);
        scaleBaselineGeneratedField(result, "gear2", ratio(-0.025 * profile.gear), note);
        reduceAccelLock(result, delta(3.0 * profile.diff), note);
        scaleBaselineGeneratedField(result, "rearBump", ratio(-0.025 * profile.damping), note);
        break;
      case "brakingInstability":
        adjustBaselineGeneratedField(result, "brakeBalance", (value) => clamp(value + delta(2.0 * profile.brake), 45, 65), note);
        reduceDecelLock(result, delta(2.0 * profile.diff), note);
        scaleBaselineGeneratedField(result, "rearRebound", ratio(-0.025 * profile.damping), note);
        scaleBaselineGeneratedField(result, "rearBump", ratio(-0.025 * profile.damping), note);
        break;
      default:
        break;
    }
    return;
  }

  if (scene === "highSpeedCorner") {
    switch (symptom) {
      case "highSpeedEntryInstability":
        scaleBaselineGeneratedField(result, "rearArb", ratio(-0.035 * profile.arb), note);
        scaleBaselineGeneratedField(result, "rearRebound", ratio(-0.035 * profile.damping), note);
        adjustBaselineGeneratedField(result, "brakeBalance", (value) => clamp(value + delta(1.0 * profile.brake), 45, 65), note);
        adjustTierRecommendation(result, "rearAero", profile.aeroTierDelta, note);
        break;
      case "midUndersteer":
        adjustBaselineGeneratedField(result, "frontCamber", (value) => value - delta(0.1 * profile.camber), note);
        scaleBaselineGeneratedField(result, "frontArb", ratio(-0.025 * profile.arb), note);
        scaleBaselineGeneratedField(result, "rearArb", ratio(0.020 * profile.arb), note);
        scaleBaselineGeneratedField(result, "frontSpring", ratio(-0.010 * profile.spring), note);
        scaleBaselineGeneratedField(result, "frontRebound", ratio(-0.015 * profile.damping), note);
        scaleBaselineGeneratedField(result, "frontBump", ratio(-0.015 * profile.damping), note);
        adjustTierRecommendation(result, "frontAero", profile.aeroTierDelta, note);
        break;
      case "highSpeedOversteer":
        scaleBaselineGeneratedField(result, "rearArb", ratio(-0.045 * profile.arb), note);
        scaleBaselineGeneratedField(result, "rearRebound", ratio(-0.035 * profile.damping), note);
        scaleBaselineGeneratedField(result, "rearBump", ratio(-0.030 * profile.damping), note);
        adjustTierRecommendation(result, "rearAero", profile.aeroTierDelta, note);
        break;
      case "bodyBounce":
        ["frontBump", "rearBump", "frontRebound", "rearRebound"].forEach((key) => {
          scaleBaselineGeneratedField(result, key, ratio(-0.050 * profile.damping), note);
        });
        ["frontArb", "rearArb"].forEach((key) => {
          scaleBaselineGeneratedField(result, key, ratio(-0.030 * profile.arb), note);
        });
        adjustTierRecommendation(result, "frontRideHeight", 1, note);
        adjustTierRecommendation(result, "rearRideHeight", 1, note);
        break;
      default:
        break;
    }
  }
}

