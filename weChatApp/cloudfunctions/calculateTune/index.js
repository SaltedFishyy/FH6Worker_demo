function baselineAlignment(useCase, carClass, drivetrain, frontWeightPct, balanceBias) {
  if (equalsIgnoreCase(useCase, "Drift")) {
    if (stringValue(drivetrain).toUpperCase() === "AWD") {
      return {
        frontCamber: -4.2,
        rearCamber: -0.4,
        frontToe: 0.8,
        rearToe: 0.0,
        caster: 7.0,
      };
    }
    return {
      frontCamber: -4.5,
      rearCamber: -0.5,
      frontToe: 1.0,
      rearToe: 0.0,
      caster: 7.0,
    };
  }
  if (equalsIgnoreCase(useCase, "Rally") || equalsIgnoreCase(useCase, "Offroad")) {
    return looseSurfaceAlignment(useCase, carClass, drivetrain, frontWeightPct, balanceBias);
  }
  if (equalsIgnoreCase(useCase, "Drag")) {
    return dragAlignment(drivetrain);
  }

  const base = roadAlignmentBaseByClass(carClass);
  let frontCamber = base.frontCamber;
  let rearCamber = base.rearCamber;
  let caster = base.caster;
  const frontWeightBias = roadFrontHeavyBalanceFactor(frontWeightPct);
  frontCamber -= 0.2 * frontWeightBias;
  rearCamber += 0.1 * frontWeightBias;
  caster += 0.3 * frontWeightBias;

  switch (stringValue(drivetrain).toUpperCase()) {
    case "FWD":
      frontCamber -= 0.1;
      rearCamber += 0.1;
      caster += 0.2;
      break;
    case "RWD":
      rearCamber -= 0.1;
      break;
    default:
      break;
  }

  const balanceAxis = baselineBiasAxis(balanceBias);
  if (balanceAxis > 0) {
    frontCamber -= 0.1 * balanceAxis;
    rearCamber += 0.1 * balanceAxis;
  } else if (balanceAxis < 0) {
    frontCamber += 0.1 * Math.abs(balanceAxis);
    rearCamber -= 0.1 * Math.abs(balanceAxis);
  }

  let frontToe = 0;
  let rearToe = 0;
  if (balanceBias >= 115) frontToe = -0.1;
  if (balanceBias <= 85) rearToe = 0.1;

  return {
    frontCamber: roundTunePlanStep(clamp(frontCamber, -2.5, -1.0), 0.1),
    rearCamber: roundTunePlanStep(clamp(rearCamber, -1.8, -0.6), 0.1),
    frontToe: roundTunePlanStep(clamp(frontToe, -0.1, 0), 0.1),
    rearToe: roundTunePlanStep(clamp(rearToe, 0, 0.1), 0.1),
    caster: roundTunePlanStep(clamp(caster, 4.8, 6.8), 0.1),
  };
}

function looseSurfaceAlignment(useCase, carClass, drivetrain, frontWeightPct, balanceBias) {
  const t = classInterpolationT(carClass);
  const frontWeightBias = roadFrontHeavyBalanceFactor(frontWeightPct);
  const offroad = equalsIgnoreCase(useCase, "Offroad");
  const balanceAxis = baselineBiasAxis(balanceBias);
  let frontCamber = offroad ? lerp(-0.6, -1.1, t) : lerp(-0.9, -1.5, t);
  let rearCamber = offroad ? lerp(-0.4, -0.8, t) : lerp(-0.6, -1.0, t);
  let caster = offroad ? lerp(4.8, 5.8, t) : lerp(5.2, 6.2, t);
  frontCamber -= 0.1 * frontWeightBias;
  rearCamber += 0.05 * frontWeightBias;
  caster += 0.2 * frontWeightBias;
  if (stringValue(drivetrain).toUpperCase() === "FWD") {
    frontCamber -= 0.1;
  }
  if (balanceAxis > 0) {
    frontCamber -= 0.1 * balanceAxis;
  } else if (balanceAxis < 0) {
    rearCamber -= 0.1 * Math.abs(balanceAxis);
  }
  return {
    frontCamber: roundTunePlanStep(clamp(frontCamber, offroad ? -1.5 : -1.8, -0.4), 0.1),
    rearCamber: roundTunePlanStep(clamp(rearCamber, offroad ? -1.0 : -1.3, -0.3), 0.1),
    frontToe: 0,
    rearToe: 0,
    caster: roundTunePlanStep(clamp(caster, 4.5, 6.5), 0.1),
  };
}

