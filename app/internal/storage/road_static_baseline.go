func baselineAlignment(useCase string, carClass string, drivetrain string, frontWeightPct float64, balanceBias float64) roadAlignmentBaseline {
	switch normalizeStaticBaselineUseCase(useCase) {
	case "Drift":
		return roadAlignmentBaseline{
			frontCamber: -4.5,
			rearCamber:  -0.5,
			frontToe:    1.0,
			rearToe:     0.0,
			caster:      7.0,
		}
	case "Rally":
		return roadAlignmentBaseline{
			frontCamber: -1.2,
			rearCamber:  -0.8,
			frontToe:    0.0,
			rearToe:     0.0,
			caster:      5.8,
		}
	case "Offroad":
		return roadAlignmentBaseline{
			frontCamber: -0.8,
			rearCamber:  -0.5,
			frontToe:    0.0,
			rearToe:     0.0,
			caster:      5.2,
		}
	case "Drag":
		return roadAlignmentBaseline{
			frontCamber: 0.0,
			rearCamber:  0.0,
			frontToe:    0.0,
			rearToe:     0.0,
			caster:      6.0,
		}
	}
	frontCamber, rearCamber, caster := roadAlignmentBaseByClass(carClass)
	frontWeightBias := roadFrontHeavyBalanceFactor(frontWeightPct)
	frontCamber -= 0.2 * frontWeightBias
	rearCamber += 0.1 * frontWeightBias
	caster += 0.3 * frontWeightBias

	switch strings.ToUpper(strings.TrimSpace(drivetrain)) {
	case "FWD":
		frontCamber -= 0.1
		rearCamber += 0.1
		caster += 0.2
	case "RWD":
		rearCamber -= 0.1
	}

	balanceAxis := baselineBiasAxis(balanceBias)
	if balanceAxis > 0 {
		frontCamber -= 0.1 * balanceAxis
		rearCamber += 0.1 * balanceAxis
	} else if balanceAxis < 0 {
		frontCamber += 0.1 * math.Abs(balanceAxis)
		rearCamber -= 0.1 * math.Abs(balanceAxis)
	}

	frontToe := 0.0
	rearToe := 0.0
	if balanceBias >= 115 {
		frontToe = -0.1
	}
	if balanceBias <= 85 {
		rearToe = 0.1
	}

	return roadAlignmentBaseline{
		frontCamber: roundTunePlanStep(clamp(frontCamber, -2.5, -1.0), 0.1),
		rearCamber:  roundTunePlanStep(clamp(rearCamber, -1.8, -0.6), 0.1),
		frontToe:    roundTunePlanStep(clamp(frontToe, -0.1, 0), 0.1),
		rearToe:     roundTunePlanStep(clamp(rearToe, 0, 0.1), 0.1),
		caster:      roundTunePlanStep(clamp(caster, 4.8, 6.8), 0.1),
	}
}

