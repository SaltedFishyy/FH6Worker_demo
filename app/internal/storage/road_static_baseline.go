type RoadStaticTuneBaselineInput struct {
	CarName           string   `json:"carName"`
	VersionName       string   `json:"versionName"`
	UseCase           string   `json:"useCase,omitempty"`
	CarOrdinal        *int64   `json:"carOrdinal,omitempty"`
	CarCategory       *int64   `json:"carCategory,omitempty"`
	PI                int64    `json:"pi"`
	Drivetrain        string   `json:"drivetrain"`
	TireCompound      string   `json:"tireCompound,omitempty"`
	WeightKG          float64  `json:"weightKG"`
	FrontWeightPct    float64  `json:"frontWeightPct"`
	PowerKW           *float64 `json:"powerKW,omitempty"`
	TorqueNM          *float64 `json:"torqueNM,omitempty"`
	RedlineRPM        *float64 `json:"redlineRPM,omitempty"`
	GearCount         *int64   `json:"gearCount,omitempty"`
	TireDiameterCm    *float64 `json:"tireDiameterCm,omitempty"`
	TargetTopSpeedKmh *float64 `json:"targetTopSpeedKmh,omitempty"`

	FrontRideHeightMinCm *float64 `json:"frontRideHeightMinCm,omitempty"`
	FrontRideHeightMaxCm *float64 `json:"frontRideHeightMaxCm,omitempty"`
	RearRideHeightMinCm  *float64 `json:"rearRideHeightMinCm,omitempty"`
	RearRideHeightMaxCm  *float64 `json:"rearRideHeightMaxCm,omitempty"`

	FrontAeroMinKgf *float64 `json:"frontAeroMinKgf,omitempty"`
	FrontAeroMaxKgf *float64 `json:"frontAeroMaxKgf,omitempty"`
	RearAeroMinKgf  *float64 `json:"rearAeroMinKgf,omitempty"`
	RearAeroMaxKgf  *float64 `json:"rearAeroMaxKgf,omitempty"`

	FrontRideHeightAdjustable bool `json:"frontRideHeightAdjustable,omitempty"`
	RearRideHeightAdjustable  bool `json:"rearRideHeightAdjustable,omitempty"`
	FrontAeroAdjustable       bool `json:"frontAeroAdjustable,omitempty"`
	RearAeroAdjustable        bool `json:"rearAeroAdjustable,omitempty"`

	BalanceBias   float64 `json:"balanceBias,omitempty"`
	StiffnessBias float64 `json:"stiffnessBias,omitempty"`
	SpeedBias     float64 `json:"speedBias,omitempty"`
}

type BaselineGeneratedField struct {
	FieldKey        string   `json:"fieldKey"`
	Group           string   `json:"group"`
	Value           *float64 `json:"value,omitempty"`
	Unit            string   `json:"unit"`
	Reason          string   `json:"reason"`
	DefaultSelected bool     `json:"defaultSelected"`
}

type BaselineSkippedField struct {
	FieldKey string `json:"fieldKey"`
	Group    string `json:"group"`
	Reason   string `json:"reason"`
	Message  string `json:"message"`
}

type BaselineTierRecommendation struct {
	FieldKey   string `json:"fieldKey"`
	Group      string `json:"group"`
	Tier       string `json:"tier"`
	Reason     string `json:"reason"`
	Applicable bool   `json:"applicable"`
}

type RoadStaticTuneBaselineResult struct {
	ProfileDraft        TuneProfileInput             `json:"profileDraft"`
	Confidence          string                       `json:"confidence"`
	GeneratedFields     []BaselineGeneratedField     `json:"generatedFields"`
	TierRecommendations []BaselineTierRecommendation `json:"tierRecommendations"`
	SkippedFields       []BaselineSkippedField       `json:"skippedFields"`
	Warnings            []string                     `json:"warnings"`
	NextTestPlan        []string                     `json:"nextTestPlan"`
}

type RoadStaticTuneBaselineApplyInput struct {
	CreateNew         bool                        `json:"createNew"`
	TargetProfileID   int64                       `json:"targetProfileId"`
	BaselineInput     RoadStaticTuneBaselineInput `json:"baselineInput"`
	SelectedFieldKeys []string                    `json:"selectedFieldKeys"`
}

type RoadStaticTuneBaselineApplyResult struct {
	Profile       TuneProfile            `json:"profile"`
	AppliedFields []string               `json:"appliedFields"`
	SkippedFields []BaselineSkippedField `json:"skippedFields"`
}

func GenerateRoadStaticTuneBaseline(input RoadStaticTuneBaselineInput) (*RoadStaticTuneBaselineResult, error) {
	normalized, err := normalizeRoadStaticBaselineInput(input)
	if err != nil {
		return nil, err
	}

	carClass := classFromPI(normalized.PI)
	useCase := normalized.UseCase
	pi := normalized.PI
	weight := normalized.WeightKG
	frontWeight := normalized.FrontWeightPct
	versionName := strings.TrimSpace(normalized.VersionName)
	if versionName == "" {
		versionName = baselineDefaultVersionName(useCase)
	}

	draft := TuneProfileInput{
		CarName:        strings.TrimSpace(normalized.CarName),
		CarOrdinal:     normalized.CarOrdinal,
		CarCategory:    normalized.CarCategory,
		CarClass:       carClass,
		PI:             &pi,
		Drivetrain:     normalized.Drivetrain,
		UseCase:        useCase,
		VersionName:    versionName,
		WeightKG:       &weight,
		FrontWeightPct: &frontWeight,
		PowerKW:        normalized.PowerKW,
		TorqueNM:       normalized.TorqueNM,
		RedlineRPM:     normalized.RedlineRPM,
		Notes:          fmt.Sprintf("Generated by %s Static Baseline.", useCase),
	}
	draft = normalizeTuneProfilePower(draft)

	result := &RoadStaticTuneBaselineResult{
		ProfileDraft: draft,
		Confidence:   "high",
		Warnings: []string{
			"Baseline values use Forza in-game slider/display units, not real-world engineering units.",
		},
		NextTestPlan: []string{
			baselineNextTestPlanIntro(useCase),
			"Watch tire temperature, slip balance, brake stability, and platform bottoming first.",
			"Use gear/power diagnostics later for gearing instead of static guesses.",
		},
	}

	add := func(key string, group string, value float64, reason string) {
		setTuneProfileInputFloat(&result.ProfileDraft, key, value)
		current, _ := getTuneProfileInputFloat(result.ProfileDraft, key)
		result.GeneratedFields = append(result.GeneratedFields, BaselineGeneratedField{
			FieldKey:        key,
			Group:           group,
			Value:           current,
			Unit:            tunePlanFieldUnit(key),
			Reason:          reason,
			DefaultSelected: true,
		})
	}
	addMeta := func(key string, group string, value *float64, unit string, reason string) {
		if value == nil {
			return
		}
		result.GeneratedFields = append(result.GeneratedFields, BaselineGeneratedField{
			FieldKey:        key,
			Group:           group,
			Value:           value,
			Unit:            unit,
			Reason:          reason,
			DefaultSelected: true,
		})
	}
	skip := func(key string, group string, reason string, message string) {
		result.SkippedFields = append(result.SkippedFields, BaselineSkippedField{
			FieldKey: key,
			Group:    group,
			Reason:   reason,
			Message:  message,
		})
	}
	addTier := func(key string, group string, tier string, applicable bool, reason string) {
		result.TierRecommendations = append(result.TierRecommendations, BaselineTierRecommendation{
			FieldKey:   key,
			Group:      group,
			Tier:       tier,
			Reason:     reason,
			Applicable: applicable,
		})
		if !applicable {
			skip(key, group, "not_confirmed_adjustable", "Confirm this tuning item is unlocked before using the tier recommendation.")
		}
	}

	addMeta("weightKG", "power", result.ProfileDraft.WeightKG, "kg", "Required baseline input.")
	addMeta("frontWeightPct", "power", result.ProfileDraft.FrontWeightPct, "%", "Required baseline input.")
	addMeta("powerKW", "power", result.ProfileDraft.PowerKW, "kW", "Optional profile context.")
	addMeta("torqueNM", "power", result.ProfileDraft.TorqueNM, "Nm", "Optional profile context.")
	addMeta("redlineRPM", "power", result.ProfileDraft.RedlineRPM, "rpm", "Static gearing input and power-band context.")

	frontTirePressure, rearTirePressure := baselineTirePressures(useCase, normalized.Drivetrain, normalized.TireCompound, normalized.WeightKG)
	add("frontTirePressure", "tire", frontTirePressure, baselineReason(useCase, "cold-pressure baseline in BAR from tire compound, vehicle weight, and drivetrain split."))
	add("rearTirePressure", "tire", rearTirePressure, baselineReason(useCase, "cold-pressure baseline in BAR from tire compound, vehicle weight, and drivetrain split."))
	alignment := baselineAlignment(useCase, carClass, normalized.Drivetrain, normalized.FrontWeightPct, normalized.BalanceBias)
	add("frontCamber", "alignment", alignment.frontCamber, baselineReason(useCase, "alignment baseline from PI class, front weight distribution, drivetrain, and balance bias."))
	add("rearCamber", "alignment", alignment.rearCamber, baselineReason(useCase, "alignment baseline from PI class, front weight distribution, drivetrain, and balance bias."))
	add("frontToe", "alignment", alignment.frontToe, baselineReason(useCase, "toe baseline; only strong balance bias moves Road away from neutral, while Drift uses fixed steering response."))
	add("rearToe", "alignment", alignment.rearToe, baselineReason(useCase, "toe baseline; only strong balance bias moves Road away from neutral, while Drift uses fixed steering response."))
	add("caster", "alignment", alignment.caster, baselineReason(useCase, "caster baseline from PI class, front weight distribution, and drivetrain."))

	springFactor := springFactorForBaseline(useCase, carClass)
	frontSpring, rearSpring := baselineSprings(useCase, normalized.Drivetrain, normalized.WeightKG, normalized.FrontWeightPct, springFactor)
	add("frontSpring", "springs", frontSpring, "FH6 kgf/mm menu value calculated from vehicle weight, axle weight distribution, and PI spring factor.")
	add("rearSpring", "springs", rearSpring, "FH6 kgf/mm menu value calculated from vehicle weight, axle weight distribution, and PI spring factor.")

	frontARB, rearARB := baselineARB(useCase, normalized.FrontWeightPct, carClass, normalized.Drivetrain)
	add("frontArb", "antiroll", frontARB, baselineReason(useCase, "lateral load-transfer baseline."))
	add("rearArb", "antiroll", rearARB, baselineReason(useCase, "lateral load-transfer baseline."))

	frontRebound, rearRebound, frontBump, rearBump := baselineDamping(useCase, normalized.WeightKG, normalized.FrontWeightPct, carClass, springFactor, frontSpring, rearSpring)
	add("frontRebound", "damping", frontRebound, baselineReason(useCase, "damping slider baseline from front axle weight, PI/use-case minimum bump, spring-factor multiplier, and bump/rebound ratio."))
	add("rearRebound", "damping", rearRebound, baselineReason(useCase, "rear damping slider baseline offset from front rebound by front/rear spring display difference."))
	add("frontBump", "damping", frontBump, baselineReason(useCase, "bump slider baseline from front axle weight, PI/use-case minimum bump, and spring-factor multiplier."))
	add("rearBump", "damping", rearBump, baselineReason(useCase, "rear bump slider baseline offset from front bump by front/rear spring display difference."))

	rideTier, rideReason := baselineRideHeightTier(useCase)
	aeroTier, aeroReason := baselineAeroTier(useCase)
	addTier("frontRideHeight", "springs", rideTier, normalized.FrontRideHeightAdjustable, rideReason)
	addTier("rearRideHeight", "springs", rideTier, normalized.RearRideHeightAdjustable, rideReason)
	addTier("frontAero", "aero", aeroTier, normalized.FrontAeroAdjustable, aeroReason)
	addTier("rearAero", "aero", aeroTier, normalized.RearAeroAdjustable, aeroReason)

	add("brakeBalance", "brake", baselineBrakeBalance(useCase, normalized.Drivetrain), baselineReason(useCase, "brake-balance baseline by use case and drivetrain."))
	add("brakePressure", "brake", baselineBrakePressure(useCase), baselineReason(useCase, "brake-pressure baseline by use case."))
	appendDifferentialBaseline(result, useCase, carClass, normalized.Drivetrain, normalized.WeightKG, normalized.FrontWeightPct, normalized.BalanceBias, add, skip)

	if gearValues, reason, message := baselineGearing(normalized); len(gearValues) > 0 {
		for _, field := range gearValues {
			add(field.key, "gearing", field.value, field.reason)
		}
		gearCount := int(*normalized.GearCount)
		for i := gearCount + 1; i <= 10; i++ {
			skip(fmt.Sprintf("gear%d", i), "gearing", "locked_or_unavailable", "Gear is above the selected gear count and is treated as not unlocked.")
		}
	} else {
		skip("finalDrive", "gearing", reason, message)
		for i := 1; i <= 10; i++ {
			skip(fmt.Sprintf("gear%d", i), "gearing", reason, message)
		}
	}

	applyRoadBaselineBiases(result, normalized)

	sort.Slice(result.GeneratedFields, func(i, j int) bool {
		if result.GeneratedFields[i].Group == result.GeneratedFields[j].Group {
			return result.GeneratedFields[i].FieldKey < result.GeneratedFields[j].FieldKey
		}
		return result.GeneratedFields[i].Group < result.GeneratedFields[j].Group
	})
	result.ProfileDraft = normalizeTuneProfilePower(result.ProfileDraft)
	return result, nil
}

func (s *Store) ApplyRoadStaticTuneBaseline(input RoadStaticTuneBaselineApplyInput) (*RoadStaticTuneBaselineApplyResult, error) {
	result, err := GenerateRoadStaticTuneBaseline(input.BaselineInput)
	if err != nil {
		return nil, err
	}
	selected, err := selectedBaselineFieldSet(input.SelectedFieldKeys, result.GeneratedFields)
	if err != nil {
		return nil, err
	}
	if len(selected) == 0 {
		return nil, errors.New("at least one generated field must be selected")
	}

	applied := sortedKeys(selected)
	var profile *TuneProfile
	if input.CreateNew {
		createInput := baselineIdentityInput(result.ProfileDraft)
		if strings.TrimSpace(createInput.CarName) == "" {
			createInput.CarName = quickTuneAutoName(result.ProfileDraft)
		}
		for key := range selected {
			if err := copyBaselineField(&createInput, result.ProfileDraft, key); err != nil {
				return nil, err
			}
		}
		profile, err = s.CreateTuneProfile(createInput)
	} else {
		if input.TargetProfileID <= 0 {
			return nil, errors.New("target profile is required")
		}
		existing, err := s.GetTuneProfile(input.TargetProfileID)
		if err != nil {
			return nil, err
		}
		updateInput := existing.ToInput()
		for key := range selected {
			if err := copyBaselineField(&updateInput, result.ProfileDraft, key); err != nil {
				return nil, err
			}
		}
		profile, err = s.updateTuneProfile(input.TargetProfileID, updateInput, staticBaselineChangeReason(result.ProfileDraft.UseCase))
	}
	if err != nil {
		return nil, err
	}
	return &RoadStaticTuneBaselineApplyResult{
		Profile:       *profile,
		AppliedFields: applied,
		SkippedFields: result.SkippedFields,
	}, nil
}

func staticBaselineChangeReason(useCase string) string {
	switch normalizeStaticBaselineUseCase(useCase) {
	case "Drift":
		return "drift_static_baseline"
	case "Rally":
		return "rally_static_baseline"
	case "Offroad":
		return "offroad_static_baseline"
	case "Drag":
		return "drag_static_baseline"
	default:
		return "road_static_baseline"
	}
}

func normalizeRoadStaticBaselineInput(input RoadStaticTuneBaselineInput) (RoadStaticTuneBaselineInput, error) {
	input.CarName = strings.TrimSpace(input.CarName)
	input.VersionName = strings.TrimSpace(input.VersionName)
	input.UseCase = normalizeStaticBaselineUseCase(input.UseCase)
	if !isSupportedStaticBaselineUseCase(input.UseCase) {
		return input, errors.New("use case must be Road, Drift, Rally, Offroad, or Drag")
	}
	input.Drivetrain = strings.ToUpper(strings.TrimSpace(input.Drivetrain))
	input.TireCompound = normalizeTireCompoundForUseCase(input.TireCompound, input.UseCase)
	if !isSupportedTireCompound(input.TireCompound) {
		return input, fmt.Errorf("tire compound must be one of: %s", strings.Join(supportedTireCompounds(), ", "))
	}
	if input.PI < 100 || input.PI > 999 {
		return input, errors.New("PI must be between 100 and 999")
	}
	if !isWholeNumber(input.WeightKG) || input.WeightKG < 300 || input.WeightKG > 3000 {
		return input, errors.New("weight kg must be an integer between 300 and 3000")
	}
	if !isWholeNumber(input.FrontWeightPct) || input.FrontWeightPct < 1 || input.FrontWeightPct > 99 {
		return input, errors.New("front weight percentage must be an integer between 1 and 99")
	}
	if input.RedlineRPM != nil && (!isWholeNumber(*input.RedlineRPM) || *input.RedlineRPM < 1000 || *input.RedlineRPM > 20000) {
		return input, errors.New("redline RPM must be an integer between 1000 and 20000")
	}
	if input.GearCount != nil && (*input.GearCount < 2 || *input.GearCount > 10) {
		return input, errors.New("gear count must be between 2 and 10")
	}
	if input.TireDiameterCm != nil && (*input.TireDiameterCm < 40 || *input.TireDiameterCm > 120) {
		return input, errors.New("tire diameter must be between 40 cm and 120 cm")
	}
	if input.TargetTopSpeedKmh != nil {
		switch normalizeStaticBaselineUseCase(input.UseCase) {
		case "Drift":
			if !isWholeNumber(*input.TargetTopSpeedKmh) || *input.TargetTopSpeedKmh < 40 || *input.TargetTopSpeedKmh > 180 {
				return input, errors.New("target drift speed must be an integer between 40 and 180 km/h")
			}
		case "Drag":
			if !isWholeNumber(*input.TargetTopSpeedKmh) || *input.TargetTopSpeedKmh < 40 || *input.TargetTopSpeedKmh > 450 {
				return input, errors.New("target drag terminal speed must be an integer between 40 and 450 km/h")
			}
		default:
			if !isWholeNumber(*input.TargetTopSpeedKmh) || *input.TargetTopSpeedKmh <= 0 || *input.TargetTopSpeedKmh > 600 {
				return input, errors.New("target top speed must be an integer between 1 and 600")
			}
		}
	}
	input.BalanceBias = normalizeBaselineBias(input.BalanceBias)
	input.StiffnessBias = normalizeBaselineBias(input.StiffnessBias)
	input.SpeedBias = normalizeBaselineBias(input.SpeedBias)
	if err := validateBaselineBias(input.BalanceBias, "balance bias"); err != nil {
		return input, err
	}
	if err := validateBaselineBias(input.StiffnessBias, "stiffness bias"); err != nil {
		return input, err
	}
	if err := validateBaselineBias(input.SpeedBias, "speed bias"); err != nil {
		return input, err
	}
	switch input.Drivetrain {
	case "FWD", "RWD", "AWD":
	default:
		return input, errors.New("drivetrain must be FWD, RWD, or AWD")
	}
	return input, nil
}

func normalizeStaticBaselineUseCase(value string) string {
	switch strings.ToLower(strings.TrimSpace(value)) {
	case "", "road", "公路":
		return "Road"
	case "drift", "漂移":
		return "Drift"
	case "rally", "拉力":
		return "Rally"
	case "offroad", "off-road", "off road", "越野":
		return "Offroad"
	case "drag", "直线", "直線":
		return "Drag"
	default:
		return strings.TrimSpace(value)
	}
}

func isSupportedStaticBaselineUseCase(value string) bool {
	switch normalizeStaticBaselineUseCase(value) {
	case "Road", "Drift", "Rally", "Offroad", "Drag":
		return true
	default:
		return false
	}
}

