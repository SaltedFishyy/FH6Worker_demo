func baselineSprings(useCase string, drivetrain string, weightKG float64, frontWeightPct float64, springFactor float64) (float64, float64) {
	const kgToLb = 2.2046226218
	const lbInPerForzaMetricDisplayUnit = 5.59974

	weightLb := weightKG * kgToLb
	frontShare := frontWeightPct / 100
	rearShare := 1 - frontShare
	frontBalanceRatio, rearBalanceRatio := supportRatiosForUseCase(useCase, drivetrain, frontWeightPct)
	calc := func(axleShare float64) float64 {
		lbIn := weightLb * axleShare * springFactor
		return clamp(roundTunePlanStep(lbIn/lbInPerForzaMetricDisplayUnit, 0.1), 0, 999)
	}
	return clamp(roundTunePlanStep(calc(frontShare)*frontBalanceRatio, 0.1), 0, 999),
		clamp(roundTunePlanStep(calc(rearShare)*rearBalanceRatio, 0.1), 0, 999)
}

