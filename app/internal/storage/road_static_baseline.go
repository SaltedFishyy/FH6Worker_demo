func baselineDamping(useCase string, weightKG float64, frontWeightPct float64, carClass string, springFactor float64, frontSpring float64, rearSpring float64) (float64, float64, float64, float64) {
	const kgToLb = 2.2046226218

	frontAxleLb := weightKG * kgToLb * frontWeightPct / 100
	baseBump := dampingMinBump(useCase, carClass) + (frontAxleLb/200)*0.1
	frontBump := baseBump * springDampingMultiplier(springFactor)
	frontRebound := frontBump / dampingBumpRatio(useCase)
	reboundOffset, bumpOffset := rearDampingOffset(frontSpring, rearSpring)
	frontBalanceRatio, rearBalanceRatio := dampingRatiosForUseCase(useCase, frontWeightPct)
	return frontRebound * frontBalanceRatio,
		(frontRebound + reboundOffset) * rearBalanceRatio,
		frontBump * frontBalanceRatio,
		(frontBump + bumpOffset) * rearBalanceRatio
}

