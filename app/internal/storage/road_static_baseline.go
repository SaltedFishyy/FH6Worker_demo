func baselineDifferential(carClass string, drivetrain string, weightKG float64, frontWeightPct float64, balanceBias float64) roadDifferentialBaseline {
	classT := classInterpolationT(carClass)
	frontLoadBias := clamp((frontWeightPct-50)/15, -1, 1)
	heavyBias := clamp((weightKG-1400)/600, -1, 1)
	balanceAxis := baselineBiasAxis(balanceBias)
	valuePtr := func(value float64) *float64 {
		rounded := clamp(roundTunePlanStep(value, 1), 0, 100)
		return &rounded
	}

	switch strings.ToUpper(strings.TrimSpace(drivetrain)) {
	case "FWD":
		frontAccel := lerp(22, 30, classT) + 1.0*frontLoadBias + 0.5*heavyBias - 2.0*balanceAxis
		frontDecel := lerp(5, 9, classT) + 0.5*heavyBias - 1.0*balanceAxis
		return roadDifferentialBaseline{
			frontAccel: valuePtr(frontAccel),
			frontDecel: valuePtr(frontDecel),
		}
	case "RWD":
		rearAccel := lerp(44, 54, classT) - 1.0*frontLoadBias + 0.5*heavyBias + 2.0*balanceAxis
		rearDecel := lerp(26, 34, classT) - 0.5*frontLoadBias + 0.5*heavyBias + 2.0*balanceAxis
		return roadDifferentialBaseline{
			rearAccel: valuePtr(rearAccel),
			rearDecel: valuePtr(rearDecel),
		}
	case "AWD":
		frontAccel := lerp(18, 25, classT) + 0.5*frontLoadBias + 0.5*heavyBias - 2.0*balanceAxis
		frontDecel := lerp(5, 8, classT) + 0.5*heavyBias - 1.0*balanceAxis
		rearAccel := lerp(50, 60, classT) - 0.5*frontLoadBias + 0.5*heavyBias + 2.0*balanceAxis
		rearDecel := lerp(22, 30, classT) - 0.25*frontLoadBias + 0.5*heavyBias + 2.0*balanceAxis
		centerSplit := lerp(62, 70, classT) + 1.0*frontLoadBias + 2.0*balanceAxis
		return roadDifferentialBaseline{
			frontAccel:  valuePtr(frontAccel),
			frontDecel:  valuePtr(frontDecel),
			rearAccel:   valuePtr(rearAccel),
			rearDecel:   valuePtr(rearDecel),
			centerSplit: valuePtr(centerSplit),
		}
	default:
		return roadDifferentialBaseline{}
	}
}

func applyRoadBaselineBiases(result *RoadStaticTuneBaselineResult, input RoadStaticTuneBaselineInput) {
	balanceAxis := baselineBiasAxis(input.BalanceBias)
	stiffnessAxis := baselineBiasAxis(input.StiffnessBias)
	speedAxis := baselineBiasAxis(input.SpeedBias)

	if balanceAxis != 0 {
		note := fmt.Sprintf("Balance bias %.0f adjusts front/rear ratio for stability versus rotation.", input.BalanceBias)
		frontRatio := 1 - 0.08*balanceAxis
		rearRatio := 1 + 0.08*balanceAxis
		frontSupportRatio := 1 - 0.04*balanceAxis
		rearSupportRatio := 1 + 0.04*balanceAxis
		scaleBaselineGeneratedField(result, "frontArb", frontRatio, note)
		scaleBaselineGeneratedField(result, "rearArb", rearRatio, note)
		scaleBaselineGeneratedField(result, "frontSpring", frontSupportRatio, note)
		scaleBaselineGeneratedField(result, "rearSpring", rearSupportRatio, note)
		scaleBaselineGeneratedField(result, "frontRebound", frontSupportRatio, note)
		scaleBaselineGeneratedField(result, "rearRebound", rearSupportRatio, note)
		scaleBaselineGeneratedField(result, "frontBump", frontSupportRatio, note)
		scaleBaselineGeneratedField(result, "rearBump", rearSupportRatio, note)
		adjustBaselineGeneratedField(result, "brakeBalance", func(value float64) float64 {
			return clamp(value-2*balanceAxis, 45, 65)
		}, note)
	}

	if stiffnessAxis != 0 {
		note := fmt.Sprintf("Stiffness bias %.0f scales roll, spring, and damping support.", input.StiffnessBias)
		ratio := 1 + 0.12*stiffnessAxis
		for _, key := range []string{"frontArb", "rearArb", "frontSpring", "rearSpring", "frontRebound", "rearRebound", "frontBump", "rearBump"} {
			scaleBaselineGeneratedField(result, key, ratio, note)
		}
	}

	if speedAxis != 0 {
		note := fmt.Sprintf("Speed bias %.0f adjusts final drive toward top speed or acceleration while preserving gear spacing.", input.SpeedBias)
		switch normalizeStaticBaselineUseCase(input.UseCase) {
		case "Drift":
			note = fmt.Sprintf("Drift speed bias %.0f adjusts final drive toward higher-speed drift or lower-speed high-RPM drift while preserving gear spacing.", input.SpeedBias)
		case "Drag":
			note = fmt.Sprintf("Drag speed bias %.0f adjusts final drive toward terminal speed or launch acceleration while preserving gear spacing.", input.SpeedBias)
		}
		ratio := 1 + 0.08*speedAxis
		scaleBaselineGeneratedField(result, "finalDrive", ratio, note)
	}
}

func baselineBiasAxis(value float64) float64 {
	return clamp((normalizeBaselineBias(value)-100)/50, -1, 1)
}

func scaleBaselineGeneratedField(result *RoadStaticTuneBaselineResult, key string, ratio float64, note string) {
	adjustBaselineGeneratedField(result, key, func(value float64) float64 {
		return value * ratio
	}, note)
}

func adjustBaselineGeneratedField(result *RoadStaticTuneBaselineResult, key string, transform func(float64) float64, note string) {
	current, ok := getTuneProfileInputFloat(result.ProfileDraft, key)
	if !ok || current == nil {
		return
	}
	if !setTuneProfileInputFloat(&result.ProfileDraft, key, transform(*current)) {
		return
	}
	updated, _ := getTuneProfileInputFloat(result.ProfileDraft, key)
	for i := range result.GeneratedFields {
		if result.GeneratedFields[i].FieldKey == key {
			result.GeneratedFields[i].Value = updated
			result.GeneratedFields[i].Reason = appendBaselineBiasReason(result.GeneratedFields[i].Reason, note)
			return
		}
	}
}

func appendBaselineBiasReason(reason string, note string) string {
	if strings.TrimSpace(note) == "" {
		return reason
	}
	if strings.Contains(reason, note) {
		return reason
	}
	return strings.TrimSpace(reason) + " " + note
}

type baselineGearField struct {
	key    string
	value  float64
	reason string
}

func baselineGearing(input RoadStaticTuneBaselineInput) ([]baselineGearField, string, string) {
	if input.RedlineRPM == nil || input.GearCount == nil || input.TireDiameterCm == nil || input.TargetTopSpeedKmh == nil {
		return nil, "missing_gear_input", "Static gearing needs redline RPM, gear count, tire size, and target top speed."
	}
	redline := *input.RedlineRPM
	gearCount := int(*input.GearCount)
	tireDiameterCm := *input.TireDiameterCm
	targetTopSpeedKmh := *input.TargetTopSpeedKmh
	if redline < 1000 || redline > 20000 {
		return nil, "invalid_gear_input", "Redline RPM should be between 1000 and 20000."
	}
	if gearCount < 2 || gearCount > 10 {
		return nil, "invalid_gear_input", "Gear count should be between 2 and 10."
	}
	if tireDiameterCm < 40 || tireDiameterCm > 120 {
		return nil, "invalid_gear_input", "Tire size should produce a diameter between 40 cm and 120 cm."
	}
	if targetTopSpeedKmh <= 0 || targetTopSpeedKmh > 600 {
		return nil, "invalid_gear_input", "Target top speed should be between 1 km/h and 600 km/h."
	}

	circumferenceMeters := math.Pi * tireDiameterCm / 100
	targetMetersPerMinute := targetTopSpeedKmh * 1000 / 60
	wheelRPMAtTarget := targetMetersPerMinute / circumferenceMeters
	if wheelRPMAtTarget <= 0 {
		return nil, "invalid_gear_input", "Target speed and tire size do not produce a valid wheel speed."
	}

	if strings.EqualFold(input.UseCase, "Drift") {
		return baselineDriftGearing(redline, gearCount, tireDiameterCm, targetTopSpeedKmh, wheelRPMAtTarget)
	}
	if strings.EqualFold(input.UseCase, "Drag") {
		return baselineDragGearing(redline, gearCount, targetTopSpeedKmh, wheelRPMAtTarget)
	}

	topTotalRatio := redline / wheelRPMAtTarget
	finalDrive := clamp(roundTunePlanStep(topTotalRatio/0.82, 0.01), 2.20, 5.50)
	topGear := clamp(roundTunePlanStep(topTotalRatio/finalDrive, 0.01), 0.45, 6.00)
	gears := make([]float64, gearCount+1)
	gears[gearCount] = topGear
	for gear := gearCount - 1; gear >= 1; gear-- {
		retention := roadGearRetention(gearCount, gear)
		gears[gear] = gears[gear+1] / retention
	}

	useCaseLabel := normalizeStaticBaselineUseCase(input.UseCase)
	gearReason := "Segmented road static gear spread with wider low gears and progressively tighter high gears; verify with telemetry after the first test."
	finalDriveReason := "Balanced road final drive from target top speed, redline RPM, and tire size."
	if useCaseLabel == "Rally" || useCaseLabel == "Offroad" {
		gearReason = useCaseLabel + " MVP uses the generic Road static gear spread as a starting point; verify on the target route and telemetry."
		finalDriveReason = useCaseLabel + " MVP uses a generic final drive from target top speed, redline RPM, and tire size; refine after route testing."
	}

	fields := []baselineGearField{{
		key:    "finalDrive",
		value:  finalDrive,
		reason: finalDriveReason,
	}}
	for gear := 1; gear <= gearCount; gear++ {
		fields = append(fields, baselineGearField{
			key:    fmt.Sprintf("gear%d", gear),
			value:  clamp(roundTunePlanStep(gears[gear], 0.01), 0.45, 6.00),
			reason: gearReason,
		})
	}
	return fields, "", ""
}

func baselineDragGearing(redline float64, gearCount int, targetTrapSpeedKmh float64, wheelRPMAtTarget float64) ([]baselineGearField, string, string) {
	if targetTrapSpeedKmh < 40 || targetTrapSpeedKmh > 450 {
		return nil, "invalid_gear_input", "Target drag terminal speed should be between 40 km/h and 450 km/h."
	}
	topTotalRatio := redline * 0.96 / wheelRPMAtTarget
	finalDrive := clamp(roundTunePlanStep(topTotalRatio/0.88, 0.01), 2.20, 5.50)
	topGear := clamp(roundTunePlanStep(topTotalRatio/finalDrive, 0.01), 0.45, 6.00)
	gears := make([]float64, gearCount+1)
	gears[gearCount] = topGear
	for gear := gearCount - 1; gear >= 1; gear-- {
		retention := dragGearRetention(gearCount, gear)
		gears[gear] = gears[gear+1] / retention
	}

	fields := []baselineGearField{{
		key:    "finalDrive",
		value:  finalDrive,
		reason: "Drag final drive targets the selected terminal speed near redline while keeping launch gears usable.",
	}}
	for gear := 1; gear <= gearCount; gear++ {
		fields = append(fields, baselineGearField{
			key:    fmt.Sprintf("gear%d", gear),
			value:  clamp(roundTunePlanStep(gears[gear], 0.01), 0.45, 6.00),
			reason: "Drag static gear spread keeps shift RPM tighter than Road and targets strong acceleration through the selected terminal speed.",
		})
	}
	return fields, "", ""
}

func dragGearRetention(gearCount int, shiftIndex int) float64 {
	extra := clamp(float64(gearCount-5), 0, 5)
	low := 0.76 + 0.008*extra
	high := 0.88 + 0.010*extra
	if gearCount <= 2 {
		return high
	}
	t := float64(shiftIndex-1) / float64(gearCount-2)
	smooth := t * t * (3 - 2*t)
	return low + (high-low)*smooth
}

func baselineDriftGearing(redline float64, gearCount int, tireDiameterCm float64, targetDriftSpeedKmh float64, wheelRPMAtTarget float64) ([]baselineGearField, string, string) {
	if targetDriftSpeedKmh < 40 || targetDriftSpeedKmh > 180 {
		return nil, "invalid_gear_input", "Target drift speed should be between 40 km/h and 180 km/h."
	}
	coreGear := driftCoreGear(gearCount)
	coreTotalRatio := redline * 0.88 / wheelRPMAtTarget
	finalDrive := clamp(roundTunePlanStep(coreTotalRatio/driftDesiredCoreGear(coreGear), 0.01), 2.20, 5.50)
	coreRatio := clamp(roundTunePlanStep(coreTotalRatio/finalDrive, 0.01), 0.45, 6.00)

	gears := make([]float64, gearCount+1)
	gears[coreGear] = coreRatio
	for gear := coreGear - 1; gear >= 1; gear-- {
		gears[gear] = gears[gear+1] / driftLowerGearRetention(gear)
	}
	for gear := coreGear + 1; gear <= gearCount; gear++ {
		gears[gear] = gears[gear-1] * driftUpperGearRetention(gear-1, coreGear)
	}

	fields := []baselineGearField{{
		key:    "finalDrive",
		value:  finalDrive,
		reason: fmt.Sprintf("Drift final drive targets about 88%% redline in gear %d at the selected drift speed.", coreGear),
	}}
	for gear := 1; gear <= gearCount; gear++ {
		fields = append(fields, baselineGearField{
			key:    fmt.Sprintf("gear%d", gear),
			value:  clamp(roundTunePlanStep(gears[gear], 0.01), 0.45, 6.00),
			reason: fmt.Sprintf("Drift static gearing centers gear %d around high RPM at %.0f km/h; lower gears are shorter for initiation and upper gears are transition gears.", coreGear, targetDriftSpeedKmh),
		})
	}
	return fields, "", ""
}

func driftCoreGear(gearCount int) int {
	switch {
	case gearCount <= 4:
		return 2
	case gearCount <= 6:
		return 3
	default:
		return 4
	}
}

func driftDesiredCoreGear(coreGear int) float64 {
	switch coreGear {
	case 2:
		return 2.05
	case 3:
		return 1.65
	default:
		return 1.35
	}
}

func driftLowerGearRetention(shiftIndex int) float64 {
	switch shiftIndex {
	case 1:
		return 0.68
	case 2:
		return 0.72
	case 3:
		return 0.76
	default:
		return 0.78
	}
}

func driftUpperGearRetention(shiftIndex int, coreGear int) float64 {
	switch shiftIndex - coreGear {
	case 0:
		return 0.82
	case 1:
		return 0.86
	case 2:
		return 0.89
	case 3:
		return 0.91
	default:
		return 0.93
	}
}

func roadGearRetention(gearCount int, shiftIndex int) float64 {
	if gearCount <= 2 {
		return 0.78
	}
	extra := clamp(float64(gearCount-5), 0, 5)
	low := 0.70 + 0.01*extra
	high := 0.84 + 0.016*extra
	t := clamp(float64(shiftIndex-1)/float64(gearCount-2), 0, 1)
	smooth := t * t * (3 - 2*t)
	return low + (high-low)*smooth
}

func rangeBaselineValue(min *float64, max *float64, ratio float64) (float64, bool) {
	if min == nil || max == nil || *max <= *min {
		return 0, false
	}
	return *min + (*max-*min)*ratio, true
}

func midpointBaselineValue(min *float64, max *float64) (float64, bool) {
	if min == nil || max == nil || *max <= *min {
		return 0, false
	}
	return (*min + *max) / 2, true
}

