func springDampingMultiplier(springFactor float64) float64 {
	return 1 + math.Max(0, springFactor-0.30)*2.0
}

func rearDampingOffset(frontSpring float64, rearSpring float64) (float64, float64) {
	avg := (frontSpring + rearSpring) / 2
	diffPct := 0.0
	if avg > 0 {
		diffPct = math.Abs(frontSpring-rearSpring) / avg * 100
	}
	reboundOffset := 0.2
	bumpOffset := 0.1
	switch {
	case diffPct > 40:
		reboundOffset = 1.2
		bumpOffset = 0.8
	case diffPct > 35:
		reboundOffset = 0.6
		bumpOffset = 0.4
	case diffPct > 1.5:
		reboundOffset = 0.3
		bumpOffset = 0.2
	}
	if frontSpring > rearSpring {
		return -reboundOffset, -bumpOffset
	}
	if rearSpring > frontSpring {
		return reboundOffset, bumpOffset
	}
	return -0.2, -0.1
}

