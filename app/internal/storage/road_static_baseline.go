func selectedBaselineFieldSet(keys []string, generated []BaselineGeneratedField) (map[string]bool, error) {
	allowed := map[string]bool{}
	defaults := map[string]bool{}
	for _, field := range generated {
		allowed[field.FieldKey] = true
		if field.DefaultSelected {
			defaults[field.FieldKey] = true
		}
	}
	if len(keys) == 0 {
		return defaults, nil
	}
	selected := map[string]bool{}
	for _, key := range keys {
		key = strings.TrimSpace(key)
		if key == "" {
			continue
		}
		if !allowed[key] {
			return nil, fmt.Errorf("selected field %q was not generated", key)
		}
		selected[key] = true
	}
	return selected, nil
}

func copyBaselineField(target *TuneProfileInput, source TuneProfileInput, key string) error {
	if value, ok := getTuneProfileInputFloat(source, key); ok && value != nil {
		if setTuneProfileInputFloat(target, key, *value) {
			return nil
		}
		switch key {
		case "weightKG":
			target.WeightKG = value
		case "frontWeightPct":
			target.FrontWeightPct = value
		case "powerKW":
			target.PowerKW = value
		case "torqueNM":
			target.TorqueNM = value
		case "redlineRPM":
			target.RedlineRPM = value
		default:
			return fmt.Errorf("unsupported baseline field %q", key)
		}
		return nil
	}
	return fmt.Errorf("baseline field %q has no value", key)
}

